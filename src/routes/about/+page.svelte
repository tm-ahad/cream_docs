<div class="section py-4">
    <div class="container mx-auto px-4">
        <h2 id="features" class="text-2xl font-bold mb-4">Features</h2>
        <ul class="mt-4 list-disc list-inside">
            <li><strong>Compiled JavaScript with Zero Abstraction</strong>: Cream generates JavaScript code without introducing unnecessary abstractions, resulting in efficient and maintainable code.</li>
            <li><strong>All-in-One HTML File</strong>: All JavaScript and markup are embedded in a single HTML file, simplifying deployment and distribution of applications.</li>
            <li><strong>Reactive States Without Weird APIs</strong>: Cream provides reactive state management without relying on complex or unfamiliar APIs, making it easy to manage application state.</li>
            <li><strong>Very Small JavaScript Script with Zero Dependencies</strong>: Cream produces lightweight JavaScript code with no external dependencies, ensuring fast load times and minimal overhead.</li>
            <li><strong>Standard Library</strong>: Cream includes a standard library with functions for tasks such as fetching data, error handling, data extraction, hashing, and cycling data, streamlining common development tasks.</li>
            <li><strong>Build Full-Stack Web Apps</strong>: Cream can be used to build full-stack web applications in conjunction with any backend framework, offering flexibility and interoperability.</li>
            <li><strong>No <code>node_modules</code> Required</strong>: Cream does not rely on <code>node_modules</code> for its functionality, eliminating unnecessary dependencies and simplifying project setup. This is particularly beneficial for projects that do not use TypeScript or Tailwind CSS.</li>
            <li><strong>Blazingly Fast and Memory Safe</strong>: Cream is built with Rust, providing excellent performance, memory safety, and reliability.</li>
            <li><strong>Relatively Newer Syntax</strong>: Cream introduces a modern syntax that is easy to learn and use, providing developers with powerful language features and expressive constructs.</li>
            <li><strong>No Bundler Required</strong>: Cream eliminates the need for bundlers, enabling simpler and more efficient build processes.</li>
        </ul>
